package Week_05;

public class ExampleOfForLoop {

    public static void main(String[] args) {
        /*
        Example of a for loop that will print out the current value of variable i,
        the value of variable i will change depending on current loop.
        */
        for (int i = 0; i < 5; i++) {
            System.out.println("current value of i is: " + i);
        }

    }

}
