package Week_05;

public class WhileLoopExample {

    public static void main(String[] args) {
        /*
        an example of a while loop that will repeat 5 times and display 
        value of variable x
        */
        int x = 0;
        while (x < 5) {
            System.out.println("value of x is: " + x);
            x++;
        }
    }

}
